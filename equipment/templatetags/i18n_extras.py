@register.filter(name="tr")
def translate(lang_code, key):
    lang = (lang_code or "ko").strip().lower()
    if lang not in I18N:
        lang = "ko"
    return I18N.get(lang, I18N["ko"]).get(key, I18N["ko"].get(key, key))

